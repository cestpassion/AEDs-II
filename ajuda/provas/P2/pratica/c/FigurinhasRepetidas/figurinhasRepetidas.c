/* Figurinhas Repetidas

Você sabe como funciona. Figurinhas são compradas em pacotes e você nunca sabe quais são as que vêm no pacote fechado.
Por esse motivo, há muitas figurinhas repetidas. Considere que uma figurinha é considerada repetida se é a segunda
cópia ou mais da mesma.

Sua coleção de figurinhas está muito bagunçada, então você deseja organizá-las. A ideia, para começo de conversa, é
contar quantas figurinhas diferentes e quantas figurinhas repetidas há na sua coleção.

Cada figurinha é representada em sua coleção como um número inteiro variando de 1 a 300. Dada sua coleção com N
figurinhas (um inteiro de 3 a 10000), cada uma delas representada por um inteiro, sua tarefa é informar quantas
figurinhas diferentes você possui e quantas são repetidas.


- Entrada:

A entrada é composta por uma primeira linha contendo um inteiro N, que pode variar de 3 a 10000. O inteiro N representa
a quantidade de figurinhas em sua coleção.
Depois disso, seguem N linhas contendo, cada uma delas, um número de figurinha, que pode ser um inteiro de 1 a 300.
A chave de parada deverá ser EOF.


- Saída:

A saída é composta de duas linhas.
A primeira delas informa a quantidade de figurinhas diferentes que você possui em sua coleção.
Já a segunda linha informa a quantidade de figurinhas repetidas que sua coleção possui.
*/

